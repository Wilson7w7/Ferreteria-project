    class timerListener implements ActionListener{
        public void actionPerformed(ActionEvent e) {
            cont++;
            jBar1.setValue(cont);
            if (cont==100) {
                tiempo.stop();
                esconder();
                MenuP pag=new MenuP();
                pag.setVisible(true);
                setVisible(false);
        
                }
        }

    }
    
    
    public void iniciosesion(){
    String usuario=jText1.getText();
    String contra=String.valueOf(jPass.getPassword());
    
    if(usuario.equals("Admin")&&contra.compareTo("1234")==0){
      jBar1.setVisible(true);
      cont=-1;
      jBar1.setValue(0);
      jBar1.setStringPainted(true);
      tiempo=new Timer(TWO_SECOND,new timerListener());
      activar();
      
    }else{
    JOptionPane.showMessageDialog(null,"Error al ingresar, usuario o contrase√±a incorrectas");
    }
}
    

