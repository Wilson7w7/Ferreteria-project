    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int row = TablaDatos.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "No se Selecciono");
        } else {
            id = Integer.parseInt((String) TablaDatos.getValueAt(row, 0).toString());
            String nom = (String) TablaDatos.getValueAt(row, 1);
            String ape = (String) TablaDatos.getValueAt(row, 2);
            nit = Integer.parseInt((String) TablaDatos.getValueAt(row, 3).toString());
            String tel = (String) TablaDatos.getValueAt(row, 4);
            String dir = (String) TablaDatos.getValueAt(row, 5);
            
            txtid.setText("" + id);
            txtNom.setText(nom);
            txtAp.setText(ape);
            txtNit.setText("" + nit);
            txtTel.setText(tel);
            txtDir.setText(dir);
            

        }
    }//GEN-LAST:event_TablaDatosMouseClicked

