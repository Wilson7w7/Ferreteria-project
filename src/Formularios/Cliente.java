    void listar() {
          String sql = "select * from cliente";
        try {
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] cliente = new Object[6];
//            String[] Titulos={"ID","DNI","NOMBRES"};         
//            model=new DefaultTableModel(null,Titulos);   
            model = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                cliente[0] = rs.getInt("idcliente");
                cliente[1] = rs.getString("nombre");
                cliente[2] = rs.getString("apellido");
                cliente[3] = rs.getInt("Nit");
                cliente[4] = rs.getString("telefono");
                cliente[5] = rs.getString("direccion");
                model.addRow(cliente);
            }
            TablaDatos.setModel(model);

        } catch (Exception e) {
        }

    }

    void Agregar() {      
        String nomb = txtNom.getText();
        String ap = txtAp.getText();
        String nit = txtNit.getText();
        String tel = txtTel.getText();
        String dir = txtDir.getText();
       
        
        try {
            if (nomb.equals("") || ap.equals("")|| nit.equals("")|| tel.equals("")|| dir.equals("")) {
                JOptionPane.showMessageDialog(null, "Debe Ingresar Datos");
                limpiarTabla(model);               
            } else {
                String sql = "insert into cliente(nombre,apellido,Nit,telefono,direccion) values('" + nomb + "','" + ap + "','" + nit + "','" + tel + "','" + dir + "')";
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Cliente Registrado con Exito");
                limpiarTabla(model);
                
            }

        } catch (Exception e) {
        }
        
        
    }

    void Modificar() {
        String nomb = txtAp.getText();
        String ap = txtNit.getText();
        String ced = txtTel.getText();
        //String Des = txtDes.getText();
        String sql = "update cliente set nombrecliente='" + nomb + "',apellidocliente='" + ap + "',cedulacliente='" + ced + "' where idcliente=" + id;
        try {
            if (nomb != null || ap != null || ced != null ) {
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Cliente Modificado");
                limpiarTabla(model);
                
            } else {
                JOptionPane.showMessageDialog(null, "Error...!!!");
            }

        } catch (Exception e) {
        }

    }

    void Eliminar() {
        String sql = "delete from cliente where idCliente=" + id;        
        int fila = TablaDatos.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null,"Cliente no Seleccionado");
        } else {
                try {
                    con = cn.getConnection();
                    st = con.createStatement();
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Cliente Eliminado");
                    limpiarTabla(model);
                    
                } catch (Exception e) {
                }
               
        }

    }

